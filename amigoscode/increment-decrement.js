// Increment and Decrement Operators

var number = 0;
// Postfix increment
console.log("postfix");
console.log(number++);
console.log(number);
console.log(number--);
console.log(number);

//prefix
console.log("Prefix ");
var numberTwo = 0;
console.log(++numberTwo);
console.log(--numberTwo);

console.log("for loop");
for (var i= 0; i<=10; i++) {
    console.log(i);
}